

class Switch{
    public static void main(String[] args) {
        
        int n =2;

        switch(n){
            case 1:
                System.out.println("Abhi");
                break;
            case 2:
                System.out.println("bhuvan");
                break;
            case 3:
                System.out.println("chaman");
                break;
            case 4:
                System.out.println("druva");
                break;
            case 5:
                System.out.println("eshwar");
                break;
            case 6:
                System.out.println("fakhar");
                break;
            case 7:
                System.out.println("govind");
                break;

            default:
                System.out.println("enter the correct number");

        }

        int day =5;

        switch(day){
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("Tuesday");
                break;
            case 3:
                System.out.println("Wednesday");
                break;
            case 4:
                System.out.println("Thursday");
                break;
            case 5:
                System.out.println("Friday");
                break;
            case 6:
                System.out.println("Saturday");
                break;
            case 7:
                System.out.println("Sunday");
                break;

            default:
                System.out.println("enter the correct number");

        }
    }
}